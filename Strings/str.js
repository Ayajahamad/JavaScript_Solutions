// Strings

// ------------------------------ 1. Searching and Extracting---------------------------------
// charAt(index): Returns the character at the specified index.
// charCodeAt(index): Returns the Unicode of the character at the specified index.
// at(index): Returns the character at the specified index (supports negative indexing).
// includes(searchString, position): Checks if a string contains a specific sequence of characters.
// indexOf(searchValue, fromIndex): Returns the index of the first occurrence of a value.
// lastIndexOf(searchValue, fromIndex): Returns the index of the last occurrence of a value.
// startsWith(searchString, position): Checks if a string starts with a specific sequence.
// endsWith(searchString, length): Checks if a string ends with a specific sequence.

// match(regexp): Returns an array of matches or null if no match is found.
// search(regexp): Returns the index of the first match or -1 if not found.

// ------------------------------- 2. Modifying Strings---------------------------------------
// replace(searchValue, newValue): Replaces the first match of searchValue with newValue.
// replaceAll(searchValue, newValue): Replaces all matches of searchValue with newValue.
// slice(start, end): Extracts a part of a string and returns it as a new string.
// substring(start, end): Similar to slice but does not accept negative indices.
// substr(start, length): Returns a substring of a specified length (deprecated).
// toLowerCase(): Converts the string to lowercase.
// toUpperCase(): Converts the string to uppercase.
// trim(): Removes whitespace from both ends of a string.
// trimStart(): Removes whitespace from the beginning of a string.
// trimEnd(): Removes whitespace from the end of a string.
// padStart(targetLength, padString): Pads the string at the start to a specified length.
// padEnd(targetLength, padString): Pads the string at the end to a specified length.

// ------------------------------- 3. Splitting and Joining----------------------------
// split(separator, limit): Splits a string into an array of substrings.
// concat(string1, ..., stringN): Combines strings into a new string.
// toString() : Converts the string to a string representation (default behavior).

// ------------------------------ 4. Accessing Properties---------------------------
// length: Returns the length of the string.

